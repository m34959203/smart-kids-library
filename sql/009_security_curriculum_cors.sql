-- 2. Расширение school_curriculum с 17 до ~150 произведений (1–11 кл., RU+KK).

-- ============================================================
-- 1. social_posts: orphan-уборка при удалении новости
-- ============================================================
-- В 001_init.sql social_posts.book_id уже SET NULL. Делаем чёткий CASCADE
-- на news_id/event_id если они есть как FK.
DO $$
BEGIN
  IF EXISTS (SELECT 1 FROM information_schema.columns
              WHERE table_name='social_posts' AND column_name='news_id') THEN
    EXECUTE 'ALTER TABLE social_posts DROP CONSTRAINT IF EXISTS social_posts_news_id_fkey';
    EXECUTE 'ALTER TABLE social_posts ADD CONSTRAINT social_posts_news_id_fkey
              FOREIGN KEY (news_id) REFERENCES news(id) ON DELETE CASCADE';
  END IF;
  IF EXISTS (SELECT 1 FROM information_schema.columns
              WHERE table_name='social_posts' AND column_name='event_id') THEN
    EXECUTE 'ALTER TABLE social_posts DROP CONSTRAINT IF EXISTS social_posts_event_id_fkey';
    EXECUTE 'ALTER TABLE social_posts ADD CONSTRAINT social_posts_event_id_fkey
              FOREIGN KEY (event_id) REFERENCES events(id) ON DELETE CASCADE';
  END IF;
END$$;

